#!/bin/bash

# Остановить выполнение, если возникла ошибка
set -e

echo "🚀 Начинаем деплой..."

# 1. Подтягиваем свежий код из GitHub
echo "📥 Получаем обновления из Git..."
git pull origin main

# 2. Пересобираем и запускаем контейнеры
# Флаг --build заставит Docker проверить изменения в Dockerfile и папке проекта
echo "🏗️ Пересборка контейнеров..."
docker compose up -d --build

# 3. Очистка старых образов (чтобы не забивать диск VPS)
echo "🧹 Очистка старых слоев..."
docker image prune -f

echo "✅ Деплой успешно завершен!"